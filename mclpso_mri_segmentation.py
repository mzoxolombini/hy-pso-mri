def _demo() -> None:
    """Quick self-test on a synthetic multi-region image."""
    print("=" * 60)
    print("MCLPSO MRI Segmentation — Demo")
    print("=" * 60)

    rng = np.random.default_rng(42)
    h, w = 128, 128
    img = np.full((h, w), 30, dtype=np.uint8)
    img[20:60, 20:60] = 90
    img[60:100, 60:100] = 150
    img[10:30, 90:110] = 200
    noise = rng.integers(-10, 10, size=(h, w))
    img = np.clip(img.astype(np.int32) + noise, 0, 255).astype(np.uint8)

    D = 3
    print(f"Image shape : {img.shape}, dtype: {img.dtype}")
    print(f"Thresholds  : D = {D}  →  {D + 1} segments")
    print()

    thresholds, segmented, fitness = segment_mri_mclpso(
        img,
        n_thresholds=D,
        mclpso_kwargs={
            "N": 20,
            "FE_max": 4_000,   # small budget for demo speed
            "seed": 1,
        },
        verbose=True,
    )

    print()
    print(f"Optimal thresholds : {thresholds.tolist()}")
    print(f"2D Renyi entropy   : {fitness:.6f}")
    print(f"Unique segments    : {np.unique(segmented).tolist()}")

    try:
        score = compute_ssim(img, segmented)
        print(f"SSIM               : {score:.4f}")
    except ImportError:
        print("SSIM: scikit-image not available, skipping.")

    # ------------------------------------------------------------------
    # Optional head-to-head comparison with HyPSO
    # ------------------------------------------------------------------
    print()
    print("Running head-to-head comparison with HyPSO ...")
    try:
        results = compare_with_hypso(img, n_thresholds=D, fe_budget=4_000, seed=1)
        print()
        print("Summary")
        print("-" * 40)
        for algo, r in results.items():
            ssim_str = f"{r['ssim']:.4f}" if not np.isnan(r["ssim"]) else "n/a"
            print(
                f"  {algo.upper():8s}  fitness={r['fitness']:.6f}  "
                f"SSIM={ssim_str}  t={r['thresholds']}"
            )
    except Exception as exc:  # noqa: BLE001
        print(f"  (comparison skipped: {exc})")

    print()
    print("Demo completed successfully.")


if __name__ == "__main__":
    _demo()
